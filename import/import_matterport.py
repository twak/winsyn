"""
Unzip and convert download matterport skyboxes to panoramas.Usage:

After downloading matterport with something like: download_mp.py /a/local/location/for/matterport --type matterport_skybox_images 

python import_matterport.py /a/local/location/for/matterport winsyn_resource_folder 

"""


def to_winsyn(matterport_root, resource_folder, whitelist):

    os.makedirs(os.path.join(resource_folder, "inside"))

    for zip in glob.glob(  os.path.join (matterport_root, "v1", "scans", "**", "**.zip") ):
        print (zip)
        archive = ZipFile(zip, 'r')
        for f in archive.namelist():
            print (f"looking at zip {f}")
            res = re.findall("/([0-9a-z]*)_skybox([0-9])", f)
            panos = {}
            if len (res) > 0 and len(res[0]) == 2:

                panos[res[0][0]] = f

            for id, zpath in panos.items():

                if not id in whitelist:
                    continue

                print (f"building pano for {id}")

                images=[]
                for i in range(6):
                    impath = re.sub( "_skybox[0-5]_", "_skybox%s_" % str(i), zpath )
                    images.append(Image.open(archive.open(impath)))

                if len(images) < 6:
                    continue

                dim=1024

                skybox = Image.new('RGB', (dim*4, dim *3) )
                for s in range (1,4):
                    skybox.paste(images[s], ( (s) * dim, dim))
                skybox.paste(images[4], (0, dim) )
                skybox.paste(images[0], (dim, 0))
                skybox.paste(images[5], (dim, dim * 2))

                equi = c2e(np.array(skybox), dim * 2, dim * 4)
                equi = Image.fromarray(equi.astype(np.uint8))
                equi.save( os.path.join(resource_folder, "inside", f"{id}.jpg"), quality=98)


